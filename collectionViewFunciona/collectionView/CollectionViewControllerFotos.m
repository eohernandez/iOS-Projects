@interface CollectionViewControllerFotos ()

@property (nonatomic, strong) NSArray *lista;

@end

@implementation CollectionViewControllerFotos

static NSString * const reuseIdentifier = @"Cell";

- (void)viewDidLoad {
    [super viewDidLoad];
    
    // Uncomment the following line to preserve selection between presentations
    // self.clearsSelectionOnViewWillAppear = NO;
    
    // Register cell classes
    //[self.collectionView registerClass:[UICollectionViewCell class] forCellWithReuseIdentifier:reuseIdentifier];
    
    // Do any additional setup after loading the view.
    UIImage *p1 = [UIImage imageNamed:@"paisaje1.jpg"];
    UIImage *p2 = [UIImage imageNamed:@"paisaje3.jpg"];
    UIImage *p3 = [UIImage imageNamed:@"paisaje5.jpg"];
    UIImage *p4 = [UIImage imageNamed:@"paisaje6.jpg"];
    
    self.lista = [[NSArray alloc] initWithObjects:p1,p2,p3,p4, nil];
    
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}


#pragma mark - Navigation

// In a storyboard-based application, you will often want to do a little preparation before navigation
- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender {
    NSIndexPath *indexPath = [[self.collectionView indexPathsForSelectedItems] objectAtIndex: 0];
    [[segue destinationViewController] setFoto: self.lista[indexPath.row]];
}


#pragma mark <UICollectionViewDataSource>

- (NSInteger)numberOfSectionsInCollectionView:(UICollectionView *)collectionView {
    return 1;
}


- (NSInteger)collectionView:(UICollectionView *)collectionView numberOfItemsInSection:(NSInteger)section {
    return self.lista.count;
}

- (UICollectionViewCell *)collectionView:(UICollectionView *)collectionView cellForItemAtIndexPath:(NSIndexPath *)indexPath {
    CollectionViewCell *cell = [collectionView dequeueReusableCellWithReuseIdentifier:reuseIdentifier forIndexPath:indexPath];
    
    // Configure the cell
    cell.imagen.image = [self.lista objectAtIndex:indexPath.row];
    
    return cell;
}

